int MultiDigits(int iNo)
{
   int iDigit = 0;
   int iResult = 1;

   if(iNo < 0)
   {
      iNo = -iNo;
   }

   while(iNo != 0)
   {
      iDigit = iNo % 10;
      if(iDigit == 0)
      {
         iDigit = 1;
      }
      iResult = iResult * iDigit;
      iNo = iNo / 10;
   }
   return iResult;
}

