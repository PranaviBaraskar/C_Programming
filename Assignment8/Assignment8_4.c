int main()
{
   int iValue = 0;
   int iRet = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   iRet = MultiDigits(iValue);

   printf("Multiplication is : %d\n",iRet);
   
   return 0;
}