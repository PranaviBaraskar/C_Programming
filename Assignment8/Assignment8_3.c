#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CountRange
//    Input             :   integer
//    Output            :   integer
//    Description       :   Count the range between 3 to 7
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   10.5.2023
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountRange(int iNo)
{
   int iDigit = 0;
   int iCount = 0;

   if(iNo < 0)
   {
      iNo = -iNo;
   }

   while(iNo != 0)
   {
      iDigit = iNo % 10;
      if((iDigit > 3) && (iDigit < 7))
      {
         iCount++;
      }
      iNo = iNo / 10;
   }
   return iCount;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   int iRet = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   iRet = CountRange(iValue);

   printf("Frequency of number in between 3 to 7 in %d is : %d\n",iValue,iRet);
   
   return 0;
}