#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CheckEven
//    Input             :   integer
//    Output            :   boolean(True/false)
//    Description       :   Check the number if it is even or odd
//    Author            :   Pranavi Ghanshyaam Baraskarr
//    Date              :   24.4.2023
//
//////////////////////////////////////////////////////////////////////////////////////////////

#define TRUE 1
#define FALSE 0

typedef int BOOL;

BOOL CheckEven (int iNo)
{
    if((iNo % 2) == 0)
    {
        return TRUE;
    }
    else
    {
        return FALSE;
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    BOOL bRet = FALSE;

    printf("Enter number\n");
    scanf("%d",&iValue);

    bRet = CheckEven(iValue);

    if(bRet == TRUE )
    {
        printf("even number\n");
    }
    else
    {
        printf("odd number\n");
    }
    return 0;

}