void Table(int iNo)
{  
    int iCnt = 0;
    int iSum = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 10; iCnt >= 1; iCnt--)
    {
        iSum = iNo * iCnt;
        printf("%d\n",iSum);
    }

}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   

   printf("Enter Number\n");
   scanf("%d",&iValue);

   Table(iValue);

   return 0;
}