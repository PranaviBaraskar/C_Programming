void strRevX(char *str)     
{
   char *small = NULL;
   char *end = NULL;
   char temp = '\0';

   small = str;
   end = str;

   while(*end != '\0')
   {
      end++;
   }
   end--;

   while(small < end)
   {
      temp = *small;
      *small = *end;
      *end = temp;

      small++;
      end--;
   }


}

