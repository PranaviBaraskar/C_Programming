int MultiFact(int iNo)
{
   int iCnt = 0;
   int iResult = 1;

   for(iCnt = 1; iCnt <= (iNo/2); iCnt++)
   {
      if((iNo % iCnt) == 0)
      {
         iResult = iResult * iCnt;
      }
   }
   return iResult;
}

