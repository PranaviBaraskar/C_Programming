#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   FactRev
//    Input             :   integer
//    Output            :   nothing returns
//    Description       :   Display Factors in decreasing format
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   04.5.2023
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void FactRev(int iNo)
{
   int iCnt = 0;

   for(iCnt = (iNo/2); iCnt >= 0; iCnt--)
   {
      if((iNo % iCnt) == 0)
      {
         printf("%d\n",iCnt);
      }
   }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   FactRev(iValue);

}