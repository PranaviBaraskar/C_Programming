////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   int iRet = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   iRet = FactDiff(iValue);

   printf("Difference between Summation of all Factors and Non Factors is : %d\n",iRet);

}