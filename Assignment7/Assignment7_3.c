#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CountTwo
//    Input             :   integer
//    Output            :   integer
//    Description       :   Count the digits how many time 2 appears
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   10.5.2023
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountTwo(int iNo)
{
   int iDigit = 0;
   int iCount = 0;

   while(iNo != 0)
   {
      iDigit = iNo % 10;
      if(iDigit == 2)
      {
         iCount++;
      }
      iNo = iNo / 10;
   }
   return iCount;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   int iRet = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   iRet = CountTwo(iValue);

   printf("Frequency of 2 in %d is : %d\n",iValue,iRet);
   return 0;
}