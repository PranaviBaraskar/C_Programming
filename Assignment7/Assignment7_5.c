#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   Count
//    Input             :   integer
//    Output            :   integer
//    Description       :   Count the digits which are less than 6
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   10.5.2023
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Count(int iNo)
{
   int iDigit = 0;
   int iCount = 0;

   while(iNo != 0)
   {
      iDigit = iNo % 10;
      if(iDigit < 6)
      {
         iCount++;
      }
      iNo = iNo / 10;
   }
   return iCount;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   int iRet = 0;

   printf("Enter Number : \n");
   scanf("%d",&iValue);

   iRet = Count(iValue);

   printf("Frequency less than 6 in %d is : %d\n",iValue,iRet);
   
   return 0;
}