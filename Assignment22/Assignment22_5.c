void Reverse(char *src, char *dest)     
{
   int iLength = 0;

   while(*src != '\0')
   {
      iLength++;
      src++;
   }
   src--;

   while(iLength != 0)
   {
      *dest = *src;
      iLength--;
      dest++;
      src--;
   }
   *dest = '\0';
   
}

