int main()
{
   char Arr[20];
   char Brr[20];
   
   printf("Enter the String :\n");
   scanf("%[^'\n']s",Arr);

   Reverse(Arr,Brr);

   printf("Reverse String is : %s\n",Brr);

   return 0;
}
