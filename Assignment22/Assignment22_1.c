////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   char Arr[20];
   int iRet = 0;
   
   printf("Enter the String :\n");
   scanf("%[^'\n']s",Arr);

   iRet = CountCapital(Arr);

   printf("%d capital letter present in string\n",iRet);

   return 0;
}
