#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CountCapital
//    Input             :   String
//    Output            :   integer
//    Description       :   display count of Capital letters
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :  
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int CountCapital(char *str)
{
   int iCount = 0;

   while(*str != '\0')
   {
      if((*str >= 'A') && (*str <= 'Z'))
      {
         iCount++;
      }
      str++;
   }

   return iCount;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
