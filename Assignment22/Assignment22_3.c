int Difference(char *str)
{
   int iCnt = 0;
   int iCnt1 = 0;
   int iFrequency = 0;

   while(*str != '\0')
   {
      if((*str >= 'a') && (*str <= 'z'))
      { 
         iCnt++;
      }
      str++;
   }

   while(*str != '\0')
   {
      if((*str >= 'A') && (*str <= 'Z'))
      { 
         iCnt1++;
      }
      str++;
   }

   iFrequency = iCnt - iCnt1;

   return iFrequency;
}

