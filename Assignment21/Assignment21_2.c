////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Problem Statement  :  Accept character from user. if Character is small display its corresponding 
//                          capital character, and if it capital then display its corresponding small.
//                          In other cases display as it is.
//                          
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   Display
//    Input             :   Character
//    Output            :   nothing return
//    Description       :   display the coresponding case of letter
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :  
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(char iCH)
{
   if((iCH >= 'a') && (iCH <= 'z'))
   {
      printf("%c\n",iCH-32);
   }
   else if((iCH >= 'A') && (iCH <= 'Z'))
   {
      printf("%c\n",iCH+32);
   }
   else
   {
      printf("%c\n",iCH);
   }
   
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   char cValue = '\0';
   
   printf("Enter the character :\n");
   scanf("%c",&cValue);

   Display(cValue);

   return 0;
}
