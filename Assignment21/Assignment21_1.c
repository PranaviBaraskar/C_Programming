#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   DisplayASCII
//    Input             :   Character
//    Output            :   nothing return
//    Description       :   Display ASCII
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   
//
