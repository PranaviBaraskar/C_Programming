void DisplayASCII()
{
   int iCount = 0;

   for(iCount = 1; iCount <= 127; iCount++)
   {
      printf("%d\t%x\t%o\n",iCount,iCount,iCount);
   }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   DisplayASCII();

   return 0;
}
