void Pattern(int iNo)
{
   int iCnt = 0;
   char ch = 'A';

   for(iCnt = 1,ch = 'A'; iCnt <= iNo; iCnt++,ch++)
   {
      printf("%c\t",ch);
   }

   printf("\n");
   
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;

   printf("Enter the frequency  :\n");
   scanf("%d",&iValue);

   Pattern(iValue);

   return 0;
}