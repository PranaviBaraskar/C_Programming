//
//    Problem Statements : Accept one number from user and print number of * on screen.
//
//////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   Display
//    Input             :   integer
//    Output            :   return nothing
//    Description       :   Display number of * on screen
//    Author            :   Pranavi Ghanshyaam Baraskarr
//    Date              :   24.4.2023
//
//////////////////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
   int i = 0;
   for(i = 0; i <= iNo; i++)
   {
      printf("*\n");
   }
}

//////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;

   printf("Enter Number :\n");
   scanf("%d",&iValue);

   Display(iValue);

   return 0;
}