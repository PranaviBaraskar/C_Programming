void MultipleDisplay(int iNo1)
{  
    int iCnt = 0;
    int iSum = 0;

    for(iCnt = 1; iCnt <= 5; iCnt++)
    {
        iSum = iNo1 * iCnt;
        printf("%d\n",iSum);
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue1 = 0;

   printf("Enter Number\n");
   scanf("%d",&iValue1);

   MultipleDisplay(iValue1);

   return 0;
}