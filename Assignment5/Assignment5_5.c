////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   MultipleDisplay
//    Input             :   integer
//    Output            :   nothing returns
//    Description       :   Display the first 5 multiples
//    Author            :   Pranavi Ghanshyaam Baraskar
