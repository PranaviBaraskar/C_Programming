////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(int iNo1)
{  
    int iCnt = 0;

    if(iNo1 < 0 )
    {
        iNo1 = -iNo1;
    }

    for(iCnt = 1; iCnt <= iNo1; iCnt++)
    {
        printf("$   *\t");
    }
    printf("\n");
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue1 = 0;

   printf("Enter Number\n");
   scanf("%d",&iValue1);

   Pattern(iValue1);

   return 0;
}