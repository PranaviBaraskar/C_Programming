#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   Pattern
//    Input             :   integer
//    Output            :   nothing returns
//    Description       :   Display the $ and * 
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   04.5.2023
