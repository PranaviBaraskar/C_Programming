void OddDisplay(int iNo1)
{  
    int iCnt = 0;

    for(iCnt = 1; iCnt <= iNo1; iCnt++)
    {
        if((iCnt % 2) != 0)
        {
            printf("%d\n",iCnt);
        }
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue1 = 0;

   printf("Enter Number\n");
   scanf("%d",&iValue1);

   OddDisplay(iValue1);

   return 0;
}