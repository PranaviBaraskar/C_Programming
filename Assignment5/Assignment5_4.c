#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   OddDisplay
//    Input             :   integer
//    Output            :   nothing returns
//    Description       :   Display the odd numbers
//    Author            :   Pranavi Ghanshyaam Baraskar
