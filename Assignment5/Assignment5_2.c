#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   Display
//    Input             :   integer
//    Output            :   nothing returns
//    Description       :   Display the number till the given input
//    Author            :   Pranavi Ghanshyaam Baraskar
