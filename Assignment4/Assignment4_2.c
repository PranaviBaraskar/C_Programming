#include<stdio.h>

typedef int BOOL;

#define TRUE 1
#define FALSE 2

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CheckGreater
//    Input             :   integer
//    Output            :   Boolean(true/false)
//    Description       :   Display the greater number
//    Author            :   Pranavi Ghanshyaam Baraskar
//    Date              :   04.5.2023
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

BOOL CheckGreater(int iNo)
{
   if(iNo >= 100)
   {
      return TRUE;
   }
   else 
   {
      return FALSE;
   }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue = 0;
   BOOL bRet = FALSE;

   printf("Enter Number\n");
   scanf("%d",&iValue);

   bRet = CheckGreater(iValue);

   if(bRet == TRUE)
   {
      printf("%d is a Greater Number\n",iValue);
   }
   else
   {
      printf("%d is a Smaller Number\n",iValue);
   }

   return 0;
}