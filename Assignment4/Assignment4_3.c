////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

typedef int BOOL;

#define TRUE 1
#define FALSE 0

BOOL CheckEqual(int iNo1, int iNo2)
{
   if(iNo1 == iNo2)
   {
      return TRUE;
   }
   else 
   {
      return FALSE;
   }
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Enter Point Function
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
   int iValue1 = 0;
   int iValue2 = 0;
   BOOL bRet = FALSE;

   printf("Enter Number\n");
   scanf("%d",&iValue1);

   printf("Enter Number\n");
   scanf("%d",&iValue2);

   bRet = CheckEqual(iValue1,iValue2);

   if(bRet == TRUE)
   {
      printf("Given number is equal\n");
   }
   else
   {
      printf("Given number is not equal\n");
   }

   return 0;
}