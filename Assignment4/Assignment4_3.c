#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//    Function Name     :   CheckEqual
//    Input             :   integer, integer
//    Output            :   Boolean(true/false)
//    Description       :   Display if number is equal
